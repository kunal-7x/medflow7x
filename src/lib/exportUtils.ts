// Export utility functions for generating CSV/reports and downloading files

export function downloadCSV(data: Record<string, any>[], filename: string, columns?: { key: string; label: string }[]) {
  const keys = columns ? columns.map(c => c.key) : Object.keys(data[0] || {});
  const headers = columns ? columns.map(c => c.label) : keys;
  
  const csvRows = [
    headers.join(','),
    ...data.map(row => 
      keys.map(key => {
        const val = row[key];
        const str = val === null || val === undefined ? '' : String(val);
        return str.includes(',') || str.includes('"') ? `"${str.replace(/"/g, '""')}"` : str;
      }).join(',')
    )
  ];
  
  const blob = new Blob([csvRows.join('\n')], { type: 'text/csv;charset=utf-8;' });
  triggerDownload(blob, `${filename}_${new Date().toISOString().split('T')[0]}.csv`);
}

export function downloadJSON(data: any, filename: string) {
  const blob = new Blob([JSON.stringify(data, null, 2)], { type: 'application/json' });
  triggerDownload(blob, `${filename}_${new Date().toISOString().split('T')[0]}.json`);
}

export function downloadTextReport(content: string, filename: string) {
  const blob = new Blob([content], { type: 'text/plain' });
  triggerDownload(blob, `${filename}_${new Date().toISOString().split('T')[0]}.txt`);
}

function triggerDownload(blob: Blob, filename: string) {
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = filename;
  document.body.appendChild(a);
  a.click();
  document.body.removeChild(a);
  URL.revokeObjectURL(url);
}

