export function generateBillingReport(bills: any[]): string {
  const total = bills.reduce((s, b) => s + (b.amount || 0), 0);
  const paid = bills.filter(b => b.status === 'paid').reduce((s, b) => s + (b.amount || 0), 0);
  const pending = bills.filter(b => b.status === 'pending').reduce((s, b) => s + (b.amount || 0), 0);
  
  return `
═══════════════════════════════════════════════
       FINANCIAL REPORT
       MedFlow General Hospital
═══════════════════════════════════════════════
Date: ${new Date().toLocaleDateString()}

SUMMARY:
  Total Revenue: $${total.toFixed(2)}
  Collected: $${paid.toFixed(2)}
  Outstanding: $${pending.toFixed(2)}
  Collection Rate: ${total > 0 ? ((paid / total) * 100).toFixed(1) : 0}%

BILL DETAILS:
${bills.map((b, i) => `
${i + 1}. ${b.patientName} - $${b.amount?.toFixed(2)} [${b.status?.toUpperCase()}]
   Due: ${b.dueDate}
`).join('')}

═══════════════════════════════════════════════
`;
}
